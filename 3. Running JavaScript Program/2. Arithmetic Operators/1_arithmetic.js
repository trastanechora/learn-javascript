// Arithmetic Operators
let a, b, add, sub, mul, div, mod;

a = 10;
b = 20;
console.log("Value a ", a);
console.log("Value b ", b);

add = a + b;
console.log("Result addition is " + add); // 30
sub = a - b;
console.log("Result subtraction is " + sub); // -10
mul = a * b;
console.log("Result multiplication is " + mul); // 200
div = a / b;
console.log("Result division is " + div); // 0.5
mod = a % b;
console.log("Result modulus is " + mod); // 10

let increment = 5;
increment++;
console.log(increment); // 6

let decrement = 6;
decrement--;
console.log(decrement) // 5
